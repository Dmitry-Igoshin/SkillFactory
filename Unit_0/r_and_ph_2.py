heads = 35  # количество голов
legs = 94  # количество ног

for r in range(heads + 1):  # количество кроликов
    ph = heads - r
    legs_curent = r * 4 + ph * 2
    print(f'Количество кроликов {r}, Количество фазанов {ph}, Количество ног {legs_curent} ')
    #  если суммарное количество ног равно заданному, то выводим результат
    if legs_curent == legs:
        print("Количество кроликов", r)
        print("Количество фазанов", ph)
        print("---")
        break

